package newcode_jianzhiOffer;
/**
 * 操作给定的二叉树，将其变换为源二叉树的镜像
 * @author purple
 *
 */
public class Demo25 {
	 public void Mirror(TreeNode root) {
		 if(root!=null &&( root.left!=null || root.right!=null) ){
			 TreeNode tmp = root.left;
			 root.left=root.right;
			 root.right=tmp;
			 Mirror(root.left);
			 Mirror(root.right);
		 }else{
			 return;
		 }
		 
	 }
	 
}
